# basic data types in python (There are three main data types in python)

# 1. Number (eg, 1000 / 200 / 300 / 3.142)
# a. Integer (eg, 100 / 200 / 300)
zip_code = 10011
print(zip_code)
# b. Float  (eg, 10.5 / 92.6)
balance = 1000.02
print(balance)

# 2. String (eg, a / b / c / mg mg)
# double quote
Country = "Myanmar"
# single quote
City = 'Yangon'
print(Country)
print(City)

# 3. Boolean (eg, True = 1 / False = 0) must start with uppercase
Are_you_over18 = True
print(Are_you_over18)

# type() function ( to check data types of variable)
age = 20
print(age)
print(type(age))
